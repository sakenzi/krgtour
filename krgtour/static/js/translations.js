// ── Language management ──────────────────────────────────────────────────────

const LANG_LABELS = { ru: 'RU', kk: 'KZ', en: 'EN' };
const LANG_FLAGS  = { ru: '🇷🇺', kk: '🇰🇿', en: '🇬🇧' };
const LANG_NAMES  = { ru: 'Русский', kk: 'Қазақша', en: 'English' };

function detectLang() {
    return localStorage.getItem('kgd_lang') || 'ru';
}

function switchLang(lang) {
    localStorage.setItem('kgd_lang', lang);
    applyTranslations();
    updateLangButton(lang);
    closeLangDropdown();
}

function updateLangButton(lang) {
    const btn = document.getElementById('langCurrentLabel');
    if (btn) btn.textContent = LANG_LABELS[lang] || 'RU';
}

function closeLangDropdown() {
    document.getElementById('langDropdown')?.classList.remove('show');
}

function applyTranslations() {
    const lang = detectLang();
    const tr = T[lang] || T.ru;

    document.querySelectorAll('[data-t]').forEach(el => {
        const key = el.dataset.t;
        if (tr[key] !== undefined) el.textContent = tr[key];
    });

    document.querySelectorAll('[data-tp]').forEach(el => {
        const key = el.dataset.tp;
        if (tr[key] !== undefined) el.placeholder = tr[key];
    });

    // Update html lang attribute
    document.documentElement.lang = lang === 'kk' ? 'kk' : lang === 'en' ? 'en' : 'ru';
}

// Expose globally
window.T = T;
window.detectLang = detectLang;
window.switchLang = switchLang;
window.applyTranslations = applyTranslations;

document.addEventListener('DOMContentLoaded', function () {
    applyTranslations();
    updateLangButton(detectLang());

    // Lang dropdown toggle
    const langBtn = document.getElementById('langBtn');
    const langDropdown = document.getElementById('langDropdown');
    if (langBtn && langDropdown) {
        langBtn.addEventListener('click', function (e) {
            e.stopPropagation();
            langDropdown.classList.toggle('show');
        });
        document.addEventListener('click', closeLangDropdown);
    }
});
